class Passing__array_to_method
    {
      public static void array_string_printer(int arry1[])
         {
              for(int i:arry1)
                  {
                        System.out.println(i);
                  }
         }
      public static void main(String[] args)
         {
          int arry[]={4234324,324,324,234,23,432,432,432,432432,4,34,34,32,432,423,432,432,432,4,324,324};
          array_string_printer(arry);
         }
    }