class For_each_loop_ex_1
     {
        public static void main(String[] args )
            {
                //creating anf initilation of array
                int arry[]={1,2,3,4,5,6,7,8};
                for(int i:arry)
                    {
                        System.out.println(i);
                    }
                for(byte j:arry)
                    {
                        System.out.println(j);
                    }
            }
     }