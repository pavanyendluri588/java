class Reverse_of_right_angle_triangle_stars
    {
       public static void main(String[] args)
          {
                 int n=10;
                 for(int i=n;i>0;i--)
                     {
                        for(int j=0;j<i;j++)
                           {
                            System.out.print("*");
                           }
                        System.out.println(" ");
                     }
          }
    }